package Classes0708;

public class Carro {
	
		String marca;
		int ano;
		String modelo;
		String cor;
		
		public String getMarca() { 
			return marca;	
   }
		public void setMarca(String marca) {
			this.marca = marca;
		}
		public int getAno() {
			return ano;
		}
		public void setAno (int ano) {
			this.ano = ano;
		}
		
		public String getModelo() {
			return modelo;
		}
		public void setModelo(String modelo) {
			this.modelo = modelo;
		}
		public String getcor() {
			return cor;
		}
		public void setcor (String cor) {
			this.cor = cor;
		}
}